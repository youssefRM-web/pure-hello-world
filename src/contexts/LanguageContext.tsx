import { apiService, endpoints } from "@/services/api";
import React, { createContext, useContext, useState, useEffect } from "react";

type Language = "en" | "de";

interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(
  undefined,
);

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
};

interface LanguageProviderProps {
  children: React.ReactNode;
}

export const LanguageProvider: React.FC<LanguageProviderProps> = ({
  children,
}) => {
  const [language, setLanguage] = useState<Language>(() => {
    try {
      const saved = localStorage.getItem("language") as Language;
      return saved && (saved === "en" || saved === "de") ? saved : "de";
    } catch (error) {
      console.warn("Error loading language from localStorage:", error);
      return "en";
    }
  });

  const handleSetLanguage = (lang: Language) => {
    setLanguage(lang);
    try {
      localStorage.setItem("language", lang);
      window.dispatchEvent(
        new CustomEvent("language-change", { detail: lang }),
      );
    } catch (error) {
      console.warn("Error saving language to localStorage:", error);
    }

    // Update language on the backend for logged-in users
    try {
      const userInfo = localStorage.getItem("userInfo");
      if (userInfo) {
        const parsed = JSON.parse(userInfo);
        const userId = parsed?.id;
        if (userId) {
          apiService
            .patch(`${endpoints.users}/${userId}/update`, { language: lang })
            .catch((err) =>
              console.warn("Failed to update language on server:", err),
            );
        }
      }
    } catch (error) {
      console.warn("Error updating language on server:", error);
    }
  };

  // Sync with localStorage changes from other providers (e.g. home page language toggle)
  useEffect(() => {
    const handleStorage = (e: StorageEvent) => {
      if (
        e.key === "language" &&
        e.newValue &&
        (e.newValue === "en" || e.newValue === "de")
      ) {
        setLanguage(e.newValue as Language);
      }
    };
    const handleLanguageChange = (e: Event) => {
      const detail = (e as CustomEvent).detail;
      if (
        detail &&
        (detail === "en" || detail === "de") &&
        detail !== language
      ) {
        setLanguage(detail as Language);
      }
    };
    window.addEventListener("storage", handleStorage);
    window.addEventListener("language-change", handleLanguageChange);
    return () => {
      window.removeEventListener("storage", handleStorage);
      window.removeEventListener("language-change", handleLanguageChange);
    };
  }, [language]);

  useEffect(() => {
    try {
      localStorage.setItem("language", language);
    } catch (error) {
      console.warn("Error persisting language:", error);
    }
  }, [language]);

  const t = (key: string): string => {
    const keys = key.split(".");
    let value: any = translations[language];

    for (const k of keys) {
      value = value?.[k];
    }

    return value || key;
  };

  return (
    <LanguageContext.Provider
      value={{ language, setLanguage: handleSetLanguage, t }}
    >
      {children}
    </LanguageContext.Provider>
  );
};

